package dev.Hilligans.ourcraft.Data.Primitives;

public class Quintet<A,B,C,D,E> {

    public A a;
    public B b;
    public C c;
    public D d;
    public E e;

    public Quintet(A a, B b, C c, D d, E e) {
        this.a = a;
        this.b = b;
        this.c = c;
        this.d = d;
        this.e = e;
    }
}
